package ie.arrays;
class Apps{
	
	public static void main(String [] args){

		System.out.println("Index\tValue");

		// show how many elements will be in array
		int bucky[] = {2,4,5,7,9};

		for(int counter = 0; counter < bucky.length; counter ++){
			System.out.println(counter + "\t " + bucky[counter]);
		}


		
	}


}